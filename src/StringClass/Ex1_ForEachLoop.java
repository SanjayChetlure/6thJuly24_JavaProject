package StringClass;
public class Ex1_ForEachLoop 
{
	public static void main(String[] args) 
	{
		String [] ar= {"mahesh","ganesh","suresh","ramesh"};
		
		for(String name:ar)
		{
			System.out.println(name);
		}
		
		int [] ar1= {10,40,50,60};
		for(int num:ar1)
		{
			System.out.println(num);
		}	
	}
}
