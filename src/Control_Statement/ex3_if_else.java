package Control_Statement;

public class ex3_if_else 
{
	public static void main(String[] args) 
	{
		int sub1=5;
		int sub2=50;
		
		//  5>=35   &  50>=35
		if(sub1>=35  & sub2>=35)               
		{
			System.out.println("Pass");
		}
		else
		{
			System.out.println("Fail");
		}
				
		  // true  & true 	->  true
		  // false & true 	-> false
		  // true  & false  -> false
		  // false & false  -> false
				
	}
}
